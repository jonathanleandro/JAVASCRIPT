var agora = new Date()
var diaSem = agora.getDay()

switch(diaSem){
    case 0: {
        if(diaSem == 0)
        console.log('Hoje é Domingo')
        break
    }
    case 1: {
        if(diaSem == 1)
        console.log('Hoje é Segunda')
        break
    }
    case 2: {
        if(diaSem == 2)
        console.log('Hoje é Terça')
        break
    }
    case 3: {
        if(diaSem == 3)
        console.log('Hoje é Quarta')
        break
    }
    case 4: {
        if(diaSem == 4)
        console.log('Hoje é Quinta')
        break
    }
    case 5: {
        if(diaSem == 5)
        console.log('Hoje é Sexta')
        break
    }
    case 6: {
        if(diaSem == 6)
        console.log('Hoje é Sabado')
        break
    }

}